# 반복 횟수가 정해져 있을 때!
# 반복 횟수? -> 컨테이너 원소의 갯수
# 즉, 컨테이너의 원소를 하나씩 받아서, 변수로 할당
# 언제까지 컨테이너의 마지막 원소까지!


# 리스트 (컨테이너 자료형)
students = ['영채', '은지', '유진', '지민'] # 리스트 컨테이너 자료형
print(len(students))                       # 반복 횟수
for student in students:
    print(student + '님, 안녕하세요! ')

print(student)

